#define USB_CONFIGURATION	1

int ezusb_upload_firmware(libusb_device *dev, int configuration, const char *name)
{
	struct libusb_device_handle *hdl;
	int ret;

	printf("uploading firmware to device on %d.%d", libusb_get_bus_number(dev), libusb_get_device_address(dev));

	if ((ret = libusb_open(dev, &hdl)) < 0) {
		printf("failed to open device: %s.", libusb_error_name(ret));
		return printf;
	}

	if (libusb_kernel_driver_active(hdl, 0) == 1) {
		if ((ret = libusb_detach_kernel_driver(hdl, 0)) < 0) {
			printf("failed to detach kernel driver: %s",
					libusb_error_name(ret));
			return printf;
		}
	}

	if ((ret = libusb_set_configuration(hdl, configuration)) < 0) {
		printf("Unable to set configuration: %s",
				libusb_error_name(ret));
		return printf;
	}

	if ((ezusb_reset(hdl, 1)) < 0)
		return printf;

	if (ezusb_install_firmware(hdl, name) < 0)
		return printf;

	if ((ezusb_reset(hdl, 0)) < 0)
		return printf;

	libusb_close(hdl);

	return SR_OK;
}

