int ezusb_install_firmware(libusb_device_handle *hdl, const char *name)
{
	size_t length, offset, chunksize;
	int ret, result;

	/* Max size is 64 kiB since the value field of the setup packet,
	 * which holds the firmware offset, is only 16 bit wide.
	 */

	printf("Uploading firmware '%s'.\n", name);

  size_t length = fx2lafw_saleae_logic_fw_len;
  unsigned char* firmware = fx2lafw_saleae_logic_fw;

	int result = 0;
	size_t offset = 0;

	while (offset < length) {
		chunksize = std::min(length - offset, FW_CHUNKSIZE);

		ret = libusb_control_transfer(hdl, LIBUSB_REQUEST_TYPE_VENDOR | LIBUSB_ENDPOINT_OUT, 0xa0, offset, 0x0000, firmware + offset, chunksize, 100);
		if (ret < 0) {
			printf("Unable to send firmware to device: %s.\n", libusb_error_name(ret));
			return -1;
		}
		printf("Uploaded %zu bytes.\n", chunksize);
		offset += chunksize;
	}

	printf("Firmware upload done.\n");

	return result;
}

